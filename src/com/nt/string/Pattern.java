package com.nt.string;

public class Pattern {
	public static void main(String[] args) {
		for(int i=0;i<=4;i++){
			for(int j=4;j>i;j--){
				System.out.print("A");
			}
			//System.out.println(" ");
			
			for(int k=4;k>=0;k--){
				
				System.out.print("D");
			}
			System.out.println(" ");
		}
		
		
	}

}
